int getCh(char c)
{
    char letterRow_col[26] = {'a', 'b', 'c', 'd', 'e', 'f', 'g', 'h', 'i', 'j', 'k', 'l', 'm', 'n', 'o', 'p', 'q', 'r', 's', 't', 'u', 'v', 'w', 'x', 'y', 'z'};
    // cout << c << " = ";
    for (int i = 0; i < 26; i++)
    {
        if (c == letterRow_col[i])
        {
            return i;
        }
    }
    return -1;
}
